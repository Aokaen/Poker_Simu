#Modificar el dataframe Triple Probabilidad
modificaTriple<-function(mano,datos,valores)
{
n1<-0
n2<-0
p1<-0
n2<-0
n1=as.numeric(mano[1,1])
p1=as.numeric(mano[1,2])
n2=as.numeric(mano[2,1])
p2=as.numeric(mano[2,2])
if(p1<p2)
{
	Num1<-n1
	Num2<-n2
	Palo1<-p1
	Palo2<-p2
}
if(p1>p2)
{
	Num2<-n1
	Num1<-n2
	Palo2<-p1
	Palo1<-p2
}
if(p1==p2)
{
  Palo1<-p1
  Palo2<-p2
	if(n1>n2)
	{
	  Num2<-n1
	  Num1<-n2
	}else	{
	  Num1<-n1
	  Num2<-n2
	}
}

n_filas=nrow(datos)
for(i in 1:n_filas)
{
	if(Num1==datos[i,1])
	{
		if(Palo1==datos[i,2])
		{
			if(Num2==datos[i,3])
			{
				if(Palo2==datos[i,4])
				{
					datos[i,5]<-valores[1]
					datos[i,6]<-valores[2]
					datos[i,7]<-valores[3]
				}
			}
		}
	}
}
	
return(datos)
}